  function bindToolInteractions(tool) {
    const input = document.getElementById("tool-input");
    const inputHighlight = document.querySelector("#tool-input-highlight code");
    const outputViewport = document.getElementById("tool-output");
    const output = document.querySelector("#tool-output code");
    const outputPanel = document.getElementById("output-panel");
    const convertButton = document.getElementById("convert-button");
    const exampleButton = document.getElementById("example-button");
    const clearButton = document.getElementById("clear-button");
    const copyButton = document.getElementById("copy-button");
    const compactButton = document.getElementById("compact-button");
    const formatButton = document.getElementById("format-button");
    const status = document.getElementById("tool-status");
    const outputMeta = document.getElementById("output-meta");
    const workspace = document.getElementById("tool-workspace");
    const mobileSelect = document.getElementById("mobile-tool-select");
    let busy = false;

    input.value = tool.example;
    updateInputHighlight(true);

    mobileSelect.addEventListener("change", function () {
      window.location.assign(`/tools/${this.value}`);
    });

    exampleButton.addEventListener("click", function () {
      input.value = tool.example;
      updateInputHighlight(true);
      input.focus();
      setStatus(status, "已填入示例，等待转换", "");
    });

    clearButton.addEventListener("click", function () {
      input.value = "";
      updateInputHighlight(true);
      clearOutput();
      setStatus(status, "输入已清空", "");
      input.focus();
    });

    convertButton.addEventListener("click", convert);
    copyButton.addEventListener("click", copyResult);
    if (compactButton) compactButton.addEventListener("click", function () {
      transformJSON("compact", "JSON 已压缩为单行");
    });
    if (formatButton) formatButton.addEventListener("click", function () {
      transformJSON("format", "JSON 已格式化展开");
    });
    input.addEventListener("input", function () {
      updateInputHighlight(false);
    });
    input.addEventListener("scroll", syncInputScroll, { passive: true });

    input.addEventListener("keydown", function (event) {
      if (event.key === "Tab") {
        event.preventDefault();
        insertAtSelection(input, "  ");
        updateInputHighlight(false);
        return;
      }
      if (event.key === "Enter" && (event.ctrlKey || event.metaKey)) {
        event.preventDefault();
        convert();
      }
    });

    function updateInputHighlight(resetScroll) {
      const source = input.value.endsWith("\n") ? input.value + " " : input.value;
      inputHighlight.innerHTML = highlightCode(source, tool.inputLanguage);
      if (resetScroll) {
        input.scrollTop = 0;
        input.scrollLeft = 0;
      }
      syncInputScroll();
    }

    function syncInputScroll() {
      inputHighlight.style.transform = `translate(${-input.scrollLeft}px, ${-input.scrollTop}px)`;
    }

    const typeName = document.getElementById("type-name");
    if (typeName) {
      typeName.addEventListener("keydown", function (event) {
        if (event.key === "Enter") {
          event.preventDefault();
          convert();
        }
      });
    }

    async function convert() {
      if (busy) return;
      const source = input.value.trim();
      if (!source) {
        clearOutput();
        setStatus(status, "请先输入需要转换的内容", "error");
        showToast("输入内容不能为空", true);
        input.focus();
        return;
      }

      busy = true;
      workspace.setAttribute("aria-busy", "true");
      convertButton.disabled = true;
      exampleButton.disabled = true;
      clearButton.disabled = true;
      convertButton.textContent = tool.local ? "正在分析…" : "正在转换…";
      setStatus(status, tool.local ? "正在本地分析结构" : "正在请求转换接口", "loading");

      try {
        const result = tool.local ? convertJSON(source) : await callAPI(tool, source);
        renderOutput(tool.outputLanguage === "JSON" ? formatJSONResult(result) : result);
        setStatus(status, "转换完成", "success");
      } catch (error) {
        clearOutput();
        const message = error && error.message ? error.message : "转换失败，请检查输入内容";
        setStatus(status, message, "error");
        showToast(message, true);
      } finally {
        busy = false;
        workspace.removeAttribute("aria-busy");
        convertButton.disabled = false;
        exampleButton.disabled = false;
        clearButton.disabled = false;
        convertButton.innerHTML = '开始转换 <span aria-hidden="true">→</span>';
      }
    }

    function convertJSON(source) {
      if (typeof window.jsonToGo !== "function") {
        throw new Error("JSON 转换模块加载失败，请刷新页面重试");
      }
      const nameField = document.getElementById("type-name");
      const type = nameField && nameField.value.trim() ? nameField.value.trim() : "AutoGenerated";
      const result = window.jsonToGo(source, type);
      if (result.error) throw new Error(`JSON 解析失败：${result.error}`);
      return result.go;
    }

    async function copyResult() {
      const value = output.textContent;
      if (!value) return;
      try {
        await copyText(value);
        showToast("结果已复制到剪贴板");
      } catch (_) {
        showToast("复制失败，请手动选择结果", true);
      }
    }

    function transformJSON(action, successMessage) {
      const formatter = window.GOTLJSON;
      if (!formatter || typeof formatter[action] !== "function") {
        showToast("JSON 格式化模块加载失败，请刷新页面重试", true);
        return;
      }

      try {
        renderOutput(formatter[action](output.textContent));
        showToast(successMessage);
      } catch (error) {
        const message = error && error.message ? error.message : "JSON 处理失败";
        showToast(message, true);
      }
    }

    function formatJSONResult(value) {
      const formatter = window.GOTLJSON;
      if (!formatter || typeof formatter.format !== "function") {
        throw new Error("JSON 格式化模块加载失败，请刷新页面重试");
      }
      return formatter.format(value);
    }

    function renderOutput(value) {
      output.innerHTML = highlightCode(value, tool.outputLanguage);
      outputViewport.scrollTop = 0;
      outputViewport.scrollLeft = 0;
      outputPanel.classList.add("has-output");
      setResultActionsDisabled(false);
      const lineCount = value ? value.split("\n").length : 0;
      setStatus(outputMeta, `${lineCount} 行 · ${value.length} 个字符`, "success");
    }

    function setResultActionsDisabled(disabled) {
      copyButton.disabled = disabled;
      if (compactButton) compactButton.disabled = disabled;
      if (formatButton) formatButton.disabled = disabled;
    }

    function clearOutput() {
      output.textContent = "";
      outputViewport.scrollTop = 0;
      outputViewport.scrollLeft = 0;
      outputPanel.classList.remove("has-output");
      setResultActionsDisabled(true);
      setStatus(outputMeta, "尚未生成内容", "");
    }
  }

  async function callAPI(tool, source) {
    const body = new URLSearchParams();
    body.set(tool.field, source);
    if (tool.option === "cache") {
      const cache = document.getElementById("cache-option");
      body.set("cache", cache && cache.checked ? "1" : "0");
    }

    const response = await fetch(tool.endpoint, {
      method: "POST",
      headers: { "Content-Type": "application/x-www-form-urlencoded;charset=UTF-8" },
      body: body.toString()
    });

    if (!response.ok) {
      throw new Error(`接口请求失败（HTTP ${response.status}）`);
    }

    let payload;
    try {
      payload = await response.json();
    } catch (_) {
      throw new Error("接口返回了无法识别的数据");
    }

    if (payload && payload.error) {
      throw new Error(payload.error);
    }
    if (!payload || payload.data === null || typeof payload.data === "undefined") {
      throw new Error("接口没有返回转换结果");
    }
    return typeof payload.data === "string" ? payload.data : JSON.stringify(payload.data, null, 2);
  }

  function setStatus(element, message, state) {
    element.textContent = message;
    element.classList.remove("success", "error", "loading");
    if (state) element.classList.add(state);
  }

  function insertAtSelection(input, text) {
    const start = input.selectionStart;
    const end = input.selectionEnd;
    input.value = input.value.slice(0, start) + text + input.value.slice(end);
    input.selectionStart = input.selectionEnd = start + text.length;
  }

  async function copyText(value) {
    if (navigator.clipboard && window.isSecureContext) {
      await navigator.clipboard.writeText(value);
      return;
    }

    const helper = document.createElement("textarea");
    helper.value = value;
    helper.setAttribute("readonly", "");
    helper.style.position = "fixed";
    helper.style.opacity = "0";
    document.body.appendChild(helper);
    helper.select();
    const copied = document.execCommand("copy");
    helper.remove();
    if (!copied) throw new Error("copy failed");
  }

  function showToast(message, isError) {
    window.clearTimeout(toastTimer);
    toast.textContent = message;
    toast.classList.toggle("error", Boolean(isError));
    toast.classList.add("visible");
    toastTimer = window.setTimeout(function () {
      toast.classList.remove("visible");
    }, 2400);
  }

  function renderNotFound() {
    document.title = "页面不存在 · GOTL";
    document.body.classList.remove("tool-page");
    app.innerHTML = `
      <main id="main-content" class="not-found">
        <span class="not-found-code">404</span>
        <h1>没有找到这个工具</h1>
        <p>请返回工具合集，选择一个可用的转换页面。</p>
        <a class="primary-link" href="/">返回工具合集 <span aria-hidden="true">→</span></a>
      </main>`;
  }
