func TestConversionEndpoints(t *testing.T) {
	r := newTestRouter()
	ddl := "CREATE TABLE users (id BIGINT NOT NULL AUTO_INCREMENT, name VARCHAR(64) NOT NULL, PRIMARY KEY (id));"
	tests := []struct {
		name     string
		path     string
		form     url.Values
		contains string
	}{
		{name: "sql to ent", path: "/sql2ent", form: url.Values{"ddl": {ddl}}, contains: "type Users struct"},
		{name: "sql to gorm", path: "/sql2gorm", form: url.Values{"ddl": {ddl}}, contains: "package model"},
		{name: "sql to go-zero", path: "/sql2gozero", form: url.Values{"ddl": {ddl}, "cache": {"0"}}, contains: "UsersModel interface"},
		{name: "sql to es", path: "/sql2es", form: url.Values{"schema": {"SELECT * FROM users WHERE status = 'active'"}}, contains: `"query"`},
		{name: "yaml to go", path: "/yaml2go", form: url.Values{"schema": {"name: gotl\nenabled: true\n"}}, contains: "type AutoGenerated struct"},
		{name: "yaml to json", path: "/yaml2json", form: url.Values{"schema": {"name: gotl\nenabled: true\nports:\n  - 8080\n  - 8081\n"}}, contains: `"name":"gotl"`},
		{name: "xml to json", path: "/xml2json", form: url.Values{"schema": {"<tool><name>gotl</name></tool>"}}, contains: `"tool"`},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			payload := performFormRequest(t, r, tt.path, tt.form)
			require.Empty(t, payload.Error)
			assert.Contains(t, payload.Data, tt.contains)
		})
	}
}

