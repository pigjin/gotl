func TestFrontendIconAssets(t *testing.T) {
	r := newTestRouter()
	tests := []struct {
		path   string
		width  int
		height int
	}{
		{path: "/assets/gotl-icon-1024.png", width: 1024, height: 1024},
		{path: "/assets/gotl-icon-128.png", width: 128, height: 128},
		{path: "/assets/favicon-32.png", width: 32, height: 32},
		{path: "/assets/apple-touch-icon-180.png", width: 180, height: 180},
	}

	for _, tt := range tests {
		t.Run(tt.path, func(t *testing.T) {
			w := httptest.NewRecorder()
			req := httptest.NewRequest(http.MethodGet, tt.path, nil)
			r.ServeHTTP(w, req)

			require.Equal(t, http.StatusOK, w.Code)
			assert.Equal(t, "image/png", w.Header().Get("Content-Type"))
			require.NotEmpty(t, w.Body.Bytes())
			config, err := png.DecodeConfig(bytes.NewReader(w.Body.Bytes()))
			require.NoError(t, err)
			assert.Equal(t, tt.width, config.Width)
			assert.Equal(t, tt.height, config.Height)
		})
	}
}

